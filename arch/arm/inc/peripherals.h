// ARM peripherals

#ifndef PERIPHERALS_HPP
#define PERIPHERALS_HPP
#include <stdint.h>

#define UART0_BASE 0x1c090000
#define GIC_BASE 0x2C000000
#define GIC_DISTRIBUTOR_BASE GIC_BASE + 0x1000
#define GIC_CPU_BASE GIC_BASE + 0x1000


#define UART0_VIRT_BASE 0x7ff00000
#define GIC_VIRT_BASE 0x7ff02000
#define GIC_DISTRIBUTOR_VIRT_BASE 0x7ff03000
#define GIC_CPU_VIRT_BASE 0x7ff04000

#define SYSTICK_BASE 0xE000E000
#define SYSTICK_VIRT_BASE 0x7ff01000

#define SYSTICK_CSR SYSTICK_VIRT_BASE + 0x10
#define SYSTICK_RVR SYSTICK_VIRT_BASE + 0x14
#define SYSTICK_CVR SYSTICK_VIRT_BASE + 0x18
#define SYSTICK_CALIB SYSTICK_VIRT_BASE + 0x1C

#define WRITE_REG(A, B) *(volatile uint32_t*)A = B;

#endif
